# try out CRC16 regs

def bitcount(v):
	v = int(v)
	v = (v&0x55555555) + ((v>>1)&0x55555555)
	v = (v&0x33333333) + ((v>>2)&0x33333333)
	v = (v&0x0F0F0F0F) + ((v>>4)&0x0F0F0F0F)
	v = (v&0x00FF00FF) + ((v>>8)&0x00FF00FF)
	v = (v&0x0000FFFF) + ((v>>16)&0x0000FFFF)
	return int(v)

refval = ((1+0xFFFF)*0xFFFF)>>1

def trypoly(p):
	a = 0xFFFF
	summ = 0
	for i in xrange(0xFFFF):
		a = (a<<1)
		if (a>=0x10000):
			a = a^p
		summ += a

	#print summ
	return refval==summ

